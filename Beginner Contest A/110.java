import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.Arrays;
import java.util.Comparator;
import java.util.List;
import java.util.stream.Collectors;

class Main {

    public static void main(String[] args) throws Exception {

        try {

            BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

            List<String> numbers = Arrays.asList(br.readLine().split(" "));
            numbers = numbers.stream().sorted(Comparator.reverseOrder()).collect(Collectors.toList());
            int result = Integer.parseInt(numbers.get(0) + numbers.get(1)) + Integer.parseInt(numbers.get(2));
            System.out.println(result);
        } catch (IOException | NumberFormatException e) {
            e.printStackTrace();
            System.exit(0);
        } catch (final Exception e) {
            e.printStackTrace();
            System.exit(0);
        }
    }
}